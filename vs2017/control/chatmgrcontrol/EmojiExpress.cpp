void EmojiExpress::EmojiParseTo(std::string src, std::string& target)
{
	/*
	1. 查找
	2. 找到后取出来字串
	3. 将字串转换成emoji码
	4. 将拼成的字符串替换
	*/
	std::string		replace("");
	std::string		strUrl("");
	std::string		strName("");
	std::string		strDst("");
	char szAnsi[buffersize] = {0};
	std::string::const_iterator	it = src.begin();
	std::string::const_iterator	end = src.end();

	std::smatch	find;
	std::regex	regImage(EMOJI_REG);

	replace = src;
	//	查找字串
	while (std::regex_search(it, end, find, regImage))
	{
		it = find[0].second;
		//	提取字串
		std::string	strFind = find[1];
		strUrl = find[2];
		if (strUrl.empty())
		{
			continue;
		}
		if (-1 == strUrl.find("emoji_face"))
			continue;
		//	获取文件名
		int pos = strUrl.rfind("/");
		if( -1 != pos){
			strName = strUrl.substr(pos+1,strUrl.length());
			utils::string_replace(strName,".png","");
		}
		strDst = utils::Hex2Emoji(strName);
		//	替换字符串
		utils::string_replace(replace,strFind,strDst);
	}
	if (replace.empty())
	{
		return;
	}
	target = replace;
}
